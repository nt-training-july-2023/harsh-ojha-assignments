package com.basics_of_java.ex4;

public class IncDecOp {
	public static void main(String[] args) {
		int i =6;
		
		//print 6
		System.out.println(i);
		
		//print 7
		System.out.println(++i);
		
		//print 7
		System.out.println(i);
		
		//print 7
		System.out.println(i++);
		
		//print 8
		System.out.println(i);
//		System.out.println();
		
	}
}
